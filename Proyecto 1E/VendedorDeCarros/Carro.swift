import Foundation

class Carro{
    
    var precio: Double
    var marca: String
    var modelo: String
    var color: String
    var estado: Bool
    
    
    init(precio:Double,marca:String,modelo:String,color:String,estado:Bool) {
        self.precio = precio
        self.marca = marca
        self.modelo = modelo
        self.color = color
        self.estado = estado
        
    }
    
    
    
    
}
